from math import *

# 삼각함수
r = radians(30) # 각도를 라디안으로 변경
print(r)
print(round(sin(r),1))
print(cos(r))
c=cos(r)
print(acos(c))

# 라디안을 각도로 변경
d = degrees(r)
print(d)

# 제곱근 
print(sqrt(2)) # root 2
print(sqrt(3)) 

# 자연로그 2
print(log(2)) # ln 2

# base 3 인 로그 2
print(log(2,3)) 

print(log(e)) # ln e

# e 의 제곱
print(exp(2))

# e의 세제곱
print(exp(3))













